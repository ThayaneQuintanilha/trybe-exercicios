// switch case default

let estado = ''

switch (estado) {
  case 'aprovada':
    console.log('Foi aprovada no processo seletivo, meus parabéns!!');
    break;
 
  case 'lista':
    console.log('Você está na lista de espera!');
    break;

  case 'reprovada':
    console.log('Infelizmente você foi reprovada no nosso processo seletivo!');
    break;

  default:
    console.log('não se aplica')
}